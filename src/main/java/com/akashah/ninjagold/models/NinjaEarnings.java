package com.akashah.ninjagold.models;

public class NinjaEarnings {
	String phrase;
	String color;
	
	public NinjaEarnings(String phrase, String color){
		this.phrase = phrase;
		this.color = color;
	}
	
	public String getPhrase() {
		return phrase;
	}
	public void setPhrase(String phrase) {
		this.phrase = phrase;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	

}
